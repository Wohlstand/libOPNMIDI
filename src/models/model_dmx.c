void opnModel_dmxLikeVolume(struct OPNVolume_t *v)
{
    uint_fast32_t volume;
    size_t i;

    volume = (v->chVol * v->chExpr * v->masterVolume) / 16129;

    if(volume > 127)
        volume = 127;

    volume = (s_dmx_volume_model[volume] + 1) << 1;
    volume = (s_dmx_volume_model[(v->vel < 128) ? v->vel : 127] * volume) >> 9;

    /* OPN has 0~127 range. As 0...63 is almost full silence, but at 64 to 127 is very closed to OPL3, just add 64.*/
    if(volume > 0)
        volume += 64;

    if(volume > 127)
        volume = 127;

    for(i = 0; i < 4; ++i)
    {
        if(v->doOp[i])
            v->tlOp[i] = 127 - (volume * (127 - (v->tlOp[i] & 127))) / 127;
    }
}
